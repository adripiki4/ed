using System;
using Calculadora;
using Gtk;

public class Operaciones
{
    public float suma(float a, float b)
    {
        return a + b;

    }

    public float resta(float a, float b)
    {
        return a - b;
    }

    public float multi(float a, float b)
    {
        return a * b;
    }

    public float divi(float a, float b)
    {
        return a / b;
    }
}